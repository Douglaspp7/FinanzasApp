function T(k) {
  return (STRINGS[window.appLang] || STRINGS.es)[k] || k;
}

function applyLang() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
      el.placeholder = T(k);
    } else {
      el.textContent = T(k);
    }
  });
}
